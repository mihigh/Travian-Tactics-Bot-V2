function criteriumFunction(rez, criterium) {
    let pop=criterium.pop*rez.pop;
    let cp=criterium.cp*rez.cp;
    let res=criterium.resorces*rez.resorces;
    let time=criterium.time*rez.time;

    let MaksPopCp=pop>cp?pop:cp;
    let MaksTRes=time>res?time:res;

    let resultMul = 1+(time+res)*(1+MaksPopCp);
    let resultDev = 1+(pop+cp)*(1+MaksTRes);
    let result=resultMul/resultDev;
    //debugger;
    return result;
}

function copy(aObject) {
    if (!aObject) {
      return aObject;
    }
  
    var bObject, v, k;
    bObject = Array.isArray(aObject) ? [] : {};
    for (k in aObject) {
      v = aObject[k];
      bObject[k] = (typeof v === "object") ? copy(v) : v;
    }
    return bObject;
  }

function getPossibleBuildings(village) {
    let rez = [];
    let buildingSpotId = -1;
    let hasResidencePalace=false;
    village.buildings.forEach(function (building, index) {
        if(building.buildingType==24||building.buildingType==25){
            hasResidencePalace=true;
        }
        if (building.buildingType !== 0) {
            if (buildingData[building.buildingType] !== undefined && buildingData[building.buildingType].upgrade[building.lvl + 1] !== undefined ) {
                let buildingTemp = JSON.parse(JSON.stringify(building));
                buildingTemp.lvl++;
                rez.push(buildingTemp);
            }
        } else {
            buildingSpotId = building.locationId;
        }
    });
    
    if (buildingSpotId !== -1) {
        buildingData.forEach(function (buildingD, index) {
            if (index < 5) return;
            if(hasResidencePalace && (index==25||index==26))return;
            
            //check if already exsist in buildingList
            for (let o = 0; o < rez.length; o++) {
                if (index == rez[o].buildingType) {
                    return;
                }
            }


            let c = false;
            village.buildings.forEach(function (building) {
                if (building.buildingType == index) c = true;
            });
            if (c) return;

            //check if requirements are met
            for (let i = 0; i < buildingD.requrements.length; i++) {
                if (buildingD.requrements[i].gid === -1) return;
                let requirementMet = false;
                for (let j = 18; j < village.buildings.length; j++) { //resource fields do not count to requirements
                    if (village.buildings[j].buildingType === buildingD.requrements[i].gid && village.buildings[j].lvl >= buildingD.requrements[i].level) {
                        requirementMet = true;
                    }
                }
                if (!requirementMet) return;
            }
            rez.push({
                buildingType: index,
                locationId: buildingSpotId,
                lvl: 1
            });
        });
    }
    return rez;
}

function evaluateBuildingList(village, buildingList) {
    let rez = {
        time: 0,
        pop: 0,
        cp: 0,
        negative: 0,
        production: 0,
        resorces: 0
    };
    let mainBuildingSpeedBonus = 100;
    
    buildingList.forEach(function (buildingL, i) {
        let building = buildingData[buildingL.buildingType].upgrade[buildingL.lvl - 1];
        if(building===undefined)debugger;
        
        //check resources
        let timeNeeded = 0;
        if (village.storage[1] < building[1]) {
            timeNeeded = (building[1] - village.storage[1]) / (village.production[1]) > timeNeeded ? (building[1] - village.storage[1]) / (village.production[1]) : timeNeeded;
        }
        if (village.storage[2] < building[2]) {
            timeNeeded = (building[2] - village.storage[2]) / (village.production[2]) > timeNeeded ? (building[2] - village.storage[2]) / (village.production[2]) : timeNeeded;
        }
        if (village.storage[3] < building[3]) {
            timeNeeded = (building[3] - village.storage[3]) / (village.production[3]) > timeNeeded ? (building[3] - village.storage[3]) / (village.production[3]) : timeNeeded;
        }
        if (village.storage[4] < building[4]) {
            timeNeeded = (building[4] - village.storage[4]) / (village.production[4]) > timeNeeded ? (building[4] - village.storage[4]) / (village.production[4]) : timeNeeded;
        }
        timeNeeded *= 3600;
        if (timeNeeded > 0) timeNeeded++;
        addResourcesByProductionTime(village, timeNeeded);

        //set resources and rez
        rez.time += building[5] / 100 ;//* mainBuildingSpeedBonus + timeNeeded;
        //debugger;
        rez.pop += building[6];
        if (buildingL.lvl - 1 === 0) {
            rez.cp += building[8];
        } else {
            rez.cp += building[8] - buildingData[buildingL.buildingType].upgrade[buildingL.lvl - 2][8];
        }
        

        addResourcesByProductionTime(village, building[5] / 100 * mainBuildingSpeedBonus);
        
        updateProduction(village, buildingL);

        

        //post check-negative resources
        if (village.storage[1] > village.storageCapacity[1]) {
            rez.negative += village.storage[1] - village.storageCapacity[1];
            village.storage[1] = village.storageCapacity[1];
        }
        if (village.storage[2] > village.storageCapacity[2]) {
            rez.negative += village.storage[2] - village.storageCapacity[2];
            village.storage[2] = village.storageCapacity[2];
        }
        if (village.storage[3] > village.storageCapacity[3]) {
            rez.negative += village.storage[3] - village.storageCapacity[3];
            village.storage[3] = village.storageCapacity[3];
        }
        if (village.storage[4] > village.storageCapacity[4]) {
            rez.negative += village.storage[4] - village.storageCapacity[4];
            village.storage[4] = village.storageCapacity[4];
        }

        village.storage[1] -= building[1];
        village.storage[2] -= building[2];
        village.storage[3] -= building[3];
        village.storage[4] -= building[4];
        
        //storage,granary and mainBuildingIncrement
        if (buildingL.buildingType == 15) {
            mainBuildingSpeedBonus = building[9];
        }
        if (buildingL.buildingType == 10) {
            village.storageCapacity[1] = building[9];
            village.storageCapacity[2] = building[9];
            village.storageCapacity[3] = building[9];
        }
        if (buildingL.buildingType == 11) {
            village.storageCapacity[4] = building[9];
        }
        rez.resorces = building[1] + building[2] + building[3] + building[4];
        
        village.buildings[buildingL.locationId].lvl++;
        if (village.buildings[buildingL.locationId].buildingType === 0) {
            village.buildings[buildingL.locationId].buildingType = buildingL.buildingType;
        } else if (village.buildings[buildingL.locationId].buildingType !== buildingL.buildingType) {
            console.log("buildingType missmatch")
        }
    });
    rez.production = village.production[1] + village.production[2] + village.production[3] + village.production[4];

    return rez;
}

function villageToSimulatorVillage(v) {
    let village = {
        production: v.production,
        storage: v.storage,
        storageCapacity: v.storageCapacity,
        buildings: []
    };
    v.buildings.forEach(function (building) {
        village.buildings.push({
            buildingType: building.buildingType * 1,
            locationId: building.locationId * 1,
            lvl: building.lvl * 1
        })
    });
    copyPropertiesNumber(village);
    return JSON.parse(JSON.stringify(village));
}

function addResourcesByProductionTime(village, time) {
    village.storage[1] += (village.production[1] / 3600) * time;
    village.storage[2] += (village.production[2] / 3600) * time;
    village.storage[3] += (village.production[3] / 3600) * time;
    village.storage[4] += (village.production[4] / 3600) * time;
}

function updateProduction(village, buildingList) {
    let building = buildingData[buildingList.buildingType].upgrade[buildingList.lvl - 1];
    if (buildingList.buildingType === 1) {
        if (buildingList.lvl - 1 === 0) {
            village.production[1] += building[9];
        } else {
            village.production[1] += building[9] - buildingData[buildingList.buildingType].upgrade[buildingList.lvl - 2][9];
        }
    }
    if (buildingList.buildingType === 2) {
        if (buildingList.lvl - 1 === 0) {
            village.production[2] += building[9];
        } else {
            village.production[2] += building[9] - buildingData[buildingList.buildingType].upgrade[buildingList.lvl - 2][9];
        }
    }
    if (buildingList.buildingType === 3) {
        if (buildingList.lvl - 1 === 0) {
            village.production[3] += building[9];
        } else {
            village.production[3] += building[9] - buildingData[buildingList.buildingType].upgrade[buildingList.lvl - 2][9];
        }
    }
    if (buildingList.buildingType === 4) {
        if (buildingList.lvl - 1 === 0) {
            village.production[4] += building[9];
        } else {
            village.production[4] += building[9] - buildingData[buildingList.buildingType].upgrade[buildingList.lvl - 2][9];
        }
    }
}

